# alias: Comparison operator
# 📚[Python3 运算符 | 菜鸟教程](https://www.runoob.com/python3/python3-basic-operators.html)
# 🔢等于 (==): 检查两个值是否相等。
result = (5 == 5)  # 结果为True
print(result)

# 🔢不等于 (!=): 检查两个值是否不相等。
result = (5 != 5)
print(result)

# 🔢大于 (>): 检查左边的值是否大于右边的值。
result = (5 > 5)
print(result)

# 🔢小于 (<): 检查左边的值是否小于右边的值。
result = (5 < 5)
print(result)

# 🔢大于等于 (>=): 检查左边的值是否大于等于右边的值。
result = (5 >= 5)
print(result)

# 🔢小于等于 (<=): 检查左边的值是否小于等于右边的值。
result = (5 <= 5)
print(result)

x = 3 > 2
print(x) # result: True
x = 3 == 2
print(x) # result: False
x = 3 != 2
print(x) # result: True
# 這是賦值表達式, 第一個等號是賦值符號, 不是比較符號