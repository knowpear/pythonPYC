# 常见运算符优先级概览（从高到低）:
# 1括号：圆括号()、方括号[]、花括号{}用于控制表达式的计算顺序，括号内的表达式总是最先计算。
# 2指数运算：如**，具有最高优先级的算术运算。
# 3乘法、除法、取模和整除运算：包括*、/、%和//。
# 4加法和减法运算：包括+和-。
# 5位运算：如<<（左移）、>>（右移）、&（按位与）、^（按位异或）、|（按位或）。
# 6比较运算符：如<、<=、>、>=、==、!=、is、is not。
# 7逻辑运算符：not、and、or，其中not优先级高于and，and优先级高于or。
# 8赋值运算符：如=, +=, -=等，从右向左结合。