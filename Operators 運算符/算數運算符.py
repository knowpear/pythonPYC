# alias: Arithmetic operators
# 🔢加法运算符 (+): 用于将两个数值相加

# 🔢减法运算符 (-): 用于从第一个数中减去第二个数。

# 🔢乘法运算符 (*): 将两个数相乘。

# 🔢除法运算符 (/): 将第一个数除以第二个数，得到一个浮点数结果。
result = 9 / 3  # 结果为3.0
print(result)

# 🔢取模运算符 (%): 返回除法的余数。
result = 10 % 3  # 结果为1
print(result)

# 🔢整除运算符 (//): 执行整数除法，丢弃任何小数部分。
result = 10 // 3  # 结果为3
print(result)

# 🔢幂运算符 (**): 计算第一个数的第二个数次幂。
result = 2 ** 3  # 结果为8，即2的3次方
print(result)

